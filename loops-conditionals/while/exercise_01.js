/*
    1. Create a while loop that will print out all the even numbers between 10 and 40.

    2. Create a while loop that will print out all the odd numbers between 10 and 40.
*/
// 1. While loop para imprimir números pares entre 10 y 40
let even = 10; 
while (even <= 40) {
    console.log(even); 
    even += 2;
}

// 2. While loop para imprimir números impares entre 10 y 40
let odd = 11;
while (odd <= 40) {
    console.log(odd);
    odd += 2; 
}
